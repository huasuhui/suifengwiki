package com.suifengwiki.Beans;

public class ArticleKind {
	
	private int articleKindId;
	
	private String articleKindName;
	
	private String kindShortName;
	
	private String makedate;
	
	private String maketime;
	
	private String modifydate;
	
	private String modifytime;
	
	

	public int getArticleKindId() {
		return articleKindId;
	}



	public void setArticleKindId(int articleKindId) {
		this.articleKindId = articleKindId;
	}



	public String getArticleKindName() {
		return articleKindName;
	}



	public void setArticleKindName(String articleKindName) {
		this.articleKindName = articleKindName;
	}



	public String getKindShortName() {
		return kindShortName;
	}



	public void setKindShortName(String kindShortName) {
		this.kindShortName = kindShortName;
	}



	public String getMakedate() {
		return makedate;
	}



	public void setMakedate(String makedate) {
		this.makedate = makedate;
	}



	public String getMaketime() {
		return maketime;
	}



	public void setMaketime(String maketime) {
		this.maketime = maketime;
	}



	public String getModifydate() {
		return modifydate;
	}



	public void setModifydate(String modifydate) {
		this.modifydate = modifydate;
	}



	public String getModifytime() {
		return modifytime;
	}



	public void setModifytime(String modifytime) {
		this.modifytime = modifytime;
	}



	public ArticleKind() {
	}

}
